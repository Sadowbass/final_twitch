package bean;

public class TagVo {
	String tag_name;
	String tag_serial;
	String findStr;
	public String getFindStr() {
		return findStr;
	}
	public void setFindStr(String findStr) {
		this.findStr = findStr;
	}
	public String getTag_name() {
		return tag_name;
	}
	public void setTag_name(String tag_name) {
		this.tag_name = tag_name;
	}
	public String getTag_serial() {
		return tag_serial;
	}
	public void setTag_serial(String tag_serial) {
		this.tag_serial = tag_serial;
	}

}
